    <script>
        $(document).ready(function(){
            // check all
            $("#check_all").change(function () {
                $("input:checkbox").prop('checked', $(this).prop("checked"));
                var values = getCheckboxsValues();
            });

            // Delete All Selected
            $('.delete_all_selected').click(function(){
                var Values = getCheckboxsValues();
                $(".select_ids").val(Values);
                var action = "{!! url(AD.'/delete_select_'.Request::segment(2)) !!}";
                $("#Delete_selected_form").attr("action",action);
            });

            // Get All Selected
            function getCheckboxsValues() {
                var checkedValues = $('.All_Checkbox:checked').map(function() {
                    return this.value;
                }).get();
                if(checkedValues=='' || checkedValues==null)
                {
                    disabled();
                }else{
                    removeDisabled();
                }
                return checkedValues;
            }

            function disabled()
            {
                $("#delete_all_selected").attr("disabled","disabled");
            }
            function removeDisabled()
            {
                $("#delete_all_selected").removeAttr("disabled");
            }
        });

    </script>
